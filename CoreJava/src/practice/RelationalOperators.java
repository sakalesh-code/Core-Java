package practice;
public class RelationalOperators{

    public static void main(String[] args) {
        int a = 7;
        int b = 6;
        // boolean result = a==b;
        // System.out.println(result);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}